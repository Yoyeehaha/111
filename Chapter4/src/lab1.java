
public class lab1 {

	public static void main(String[] args) {
		char myChar = 'b';
		switch(myChar) {
		    case 'a':
		    	System.out.println("It is an A");
		    	break;
		    case 'b':
		    	System.out.println("It is a B");
		    	break;
		    default: 
		    	System.out.println("Default case");
		    	break;
		}

	}

}
